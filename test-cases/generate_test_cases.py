# 支持的文件扩展名
DOC_EXTENSIONS = {".md", ".markdown", ".txt", ".docx", ".pdf"}


def validate_file(file_path, allowed_extensions, file_type_name):
    if not file_path:
        return None
    file_path = os.path.expanduser(file_path.strip())
    if not os.path.isabs(file_path):
        file_path = os.path.abspath(file_path)
    if not os.path.isfile(file_path):
        print(f"❌ 错误：{file_type_name}文件不存在：{file_path}")
        return None
    ext = os.path.splitext(file_path)[1].lower()
    if ext not in allowed_extensions:
        print(f"⚠️  警告：{file_type_name}文件扩展名 '{ext}' 不在支持的格式中 {allowed_extensions}")
    return file_path


def get_output_filename(doc_path=None):
    if doc_path and os.path.isfile(doc_path):
        name = os.path.splitext(os.path.basename(doc_path))[0]
        if name:
            return name
    return "AI生成的测试用例"


def read_doc_content(doc_path):
    if not doc_path:
        return None
    ext = os.path.splitext(doc_path)[1].lower()
    if ext in {".md", ".markdown", ".txt"}:
        with open(doc_path, "r", encoding="utf-8") as f:
            return f.read()
    elif ext == ".docx":
        try:
            import docx
            doc = docx.Document(doc_path)
            return "\n".join([para.text for para in doc.paragraphs])
        except ImportError:
            print("⚠️  读取 .docx 需要安装 python-docx：pip install python-docx")
            return None
    elif ext == ".pdf":
        try:
            import pdfplumber
            with pdfplumber.open(doc_path) as pdf:
                text = ""
                for page in pdf.pages:
                    text += page.extract_text() or ""
                return text
        except ImportError:
            print("⚠️  读取 .pdf 需要安装 pdfplumber：pip install pdfplumber")
            return None
    else:
        try:
            with open(doc_path, "r", encoding="utf-8") as f:
                return f.read()
        except Exception as e:
            print(f"⚠️  无法读取文件 {doc_path}：{e}")
            return None


def create_test_cases(test_cases_data, doc_path=None, sheet_title="测试用例"):
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = sheet_title

    header_font = Font(name="微软雅黑", bold=True, size=11, color="FFFFFF")
    header_fill = PatternFill(start_color="4472C4", end_color="4472C4", fill_type="solid")
    header_alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
    cell_alignment = Alignment(horizontal="left", vertical="center", wrap_text=True)
    thin_border = Border(
        left=Side(style="thin"),
        right=Side(style="thin"),
        top=Side(style="thin"),
        bottom=Side(style="thin"),
    )

    headers = ["编号", "用例标题", "前置条件", "步骤", "预期结果", "用例等级"]

    for col, header in enumerate(headers, 1):
        cell = ws.cell(row=1, column=col, value=header)
        cell.font = header_font
        cell.fill = header_fill
        cell.alignment = header_alignment
        cell.border = thin_border

    for row_idx, case in enumerate(test_cases_data, 2):
        for col_idx, value in enumerate(case, 1):
            cell = ws.cell(row=row_idx, column=col_idx, value=value)
            cell.alignment = cell_alignment
            cell.border = thin_border

    column_widths = [12, 50, 55, 45, 65, 10]
    for i, width in enumerate(column_widths, 1):
        ws.column_dimensions[get_column_letter(i)].width = width

    ws.freeze_panes = "A2"

    filename = get_output_filename(doc_path)
    output_path = f"/Users/mac/Desktop/{filename}.xlsx"
    wb.save(output_path)
    return output_path


def parse_inputs():
    parser = argparse.ArgumentParser(description="根据需求文档生成测试用例")
    parser.add_argument("files", nargs="*", type=str, help="文件路径")
    parser.add_argument("--doc", "-d", type=str, default=None, help="需求文档路径")
    args = parser.parse_args()

    doc_path = args.doc
    for file_path in (args.files or []):
        ext = os.path.splitext(file_path)[1].lower()
        if ext in DOC_EXTENSIONS:
            if doc_path is None:
                doc_path = file_path

    if not doc_path:
        print("\n❌ 错误：请提供需求文档文件")
        sys.exit(1)

    validated_doc = validate_file(doc_path, DOC_EXTENSIONS, "需求文档")
    if not validated_doc:
        print("\n❌ 错误：提供的文件无效，请检查路径后重试。")
        sys.exit(1)

    return validated_doc


